export const getTopicById = (id: string): IeltsSpeakingTopic | undefined =>
  IELTS_P1_TOPICS.find((t) => t.id === id);

export const getRandomTopics = (count: number): IeltsSpeakingTopic[] => {
  const shuffled = [...IELTS_P1_TOPICS].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};

export const getRandomQuestions = (
  count: number,
): { question: string; topicName: string; questionId: string }[] => {
  const all = IELTS_P1_TOPICS.flatMap((t) =>
    t.questions.map((q) => ({ question: q.text, topicName: t.name, questionId: q.id })),
  );
  return all.sort(() => Math.random() - 0.5).slice(0, count);
};
