export function getRandomCueCard(excludeIds: string[] = []): Part2CueCard {
  const available = PART2_CUE_CARDS.filter((c) => !excludeIds.includes(c.id));
  const pool = available.length > 0 ? available : PART2_CUE_CARDS;
  return pool[Math.floor(Math.random() * pool.length)];
}

export function getCueCardById(id: string): Part2CueCard | undefined {
  return PART2_CUE_CARDS.find((c) => c.id === id);
}
